/*
 * Function: blinkled
 * Blinks an LED connected to GPIOA Pin 5 by setting it high for 1 second and then low for 1 second.
 * Parameters: None
 * Returns: None
 */
void blinkaled(void)
{
  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_SET);
  osDelay(1000);
  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_RESET);
  osDelay(1000);
}
